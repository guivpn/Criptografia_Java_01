    private void executarRotina() {
        String mensagemTexto = mensagem.getText();
        String chaveTexto = chave.getText();
        
        if(!mensagemTexto.isEmpty() && !chaveTexto.isEmpty()) {
            int chave01 = Integer.parseInt(chaveTexto);
            String mensagemCriptografada = Criptografar.criptografar(mensagemTexto, chave01);
            String mensagemDescriptografada = Criptografar.descriptografar(mensagemCriptografada, chave01);
            resultadoTable.resultadoTabela(this,mensagemTexto, chaveTexto, mensagemCriptografada, mensagemDescriptografada);
        }
    }
    
